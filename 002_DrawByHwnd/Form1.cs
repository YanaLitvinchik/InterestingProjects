		private void button1_Click(object sender, System.EventArgs e)
		{
            int id;
            if (textBox1.Text != String.Empty)
            {
                id = Convert.ToInt32(textBox1.Text,16);
            }
            else
                id = panel1.Handle.ToInt32();
            MessageBox.Show(panel1.Handle.ToInt32().ToString());

            IntPtr i = new IntPtr(id);
			Graphics g = Graphics.FromHwnd(i); 
            
			SolidBrush brush = new SolidBrush(Color.Red);
            g.DrawString("1",new Font("ARIAL",10),brush,new PointF(0,0));
			Rectangle rectangle = new Rectangle(0, 0, 100, 100); 
			g.FillRectangle(brush, rectangle);
		}
